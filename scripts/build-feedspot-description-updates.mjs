/**
 * One-off: parse 007_feedspot_bitcoin_podcasts.sql for slug + rss_url, fetch
 * RSS channel description, print UPDATE statements for migration 008.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import Parser from "rss-parser";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const M7 = path.join(
  ROOT,
  "supabase/migrations/007_feedspot_bitcoin_podcasts.sql",
);

function sqlString(s) {
  return "'" + String(s).replace(/'/g, "''") + "'";
}

function stripHtml(html) {
  return String(html)
    .replace(/<[^>]+>/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function pickDescription(parsed) {
  const raw =
    parsed.description ||
    parsed.itunes?.summary ||
    parsed.contentSnippet ||
    "";
  let text = stripHtml(raw)
    .replace(/&nbsp;/gi, " ")
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\s+/g, " ")
    .trim();
  if (!text) return null;
  // Cap for DB / migration readability (matches tagline cap order of magnitude)
  if (text.length > 4000) return text.slice(0, 3997) + "...";
  return text;
}

function parse007(content) {
  const rows = [];
  const blockRe = /\(\s*\n\s*'([^']+)',\s*\n\s*'(?:[^']|'')*',\s*\n\s*(?:'(?:[^']|'')*'|null),\s*\n\s*'(?:[^']|'')*',\s*\n\s*'([^']+)',\s*\n/gm;
  let m;
  while ((m = blockRe.exec(content)) !== null) {
    rows.push({ slug: m[1], rss: m[2] });
  }
  return rows;
}

async function main() {
  const content = fs.readFileSync(M7, "utf8");
  const rows = parse007(content);
  if (!rows.length) {
    console.error("No rows parsed from 007");
    process.exit(1);
  }

  const parser = new Parser({
    timeout: 25000,
    headers: { "User-Agent": "btc-pod-summaries-description-sync/1" },
  });

  const updates = [];
  for (const { slug, rss } of rows) {
    let desc = null;
    try {
      const parsed = await parser.parseURL(rss);
      desc = pickDescription(parsed);
    } catch (e) {
      console.error(`skip ${slug}: ${e?.message || e}`);
    }
    if (desc) {
      updates.push(
        `update public.podcasts set description = ${sqlString(
          desc,
        )} where slug = ${sqlString(slug)} and description like ${sqlString(
          "%FeedSpot%",
        )};`,
      );
    }
    await new Promise((r) => setTimeout(r, 120));
  }

  const header = `-- Replace FeedSpot placeholder descriptions with RSS channel summaries.
-- Generated by scripts/build-feedspot-description-updates.mjs

`;

  process.stdout.write(header + updates.join("\n") + "\n");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
